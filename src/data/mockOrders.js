import { getStageProgressFromOrder } from './orderStages.js'

export function getImportProgressForOrder(orderId) {
  const entries = mockImportEntries.filter((entry) => entry.orderId === orderId)
  if (!entries.length) return null

  const furthest = entries.reduce((best, entry) =>
    entry.importStagesComplete > best.importStagesComplete ? entry : best,
  )

  return {
    stagesComplete: furthest.importStagesComplete,
    stagesTotal: furthest.importStagesTotal,
    currentStage: furthest.currentImportStage,
  }
}

export function getStageProgress(order) {
  return getStageProgressFromOrder(order)
}

export function formatCurrency(amount) {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    maximumFractionDigits: 0,
  }).format(amount)
}

export function formatOrderAmount(amount) {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(amount)
}

export function getOrderTotal(order) {
  if (!order.items?.length) return order.importSpend ?? 0
  return order.items.reduce((sum, item) => sum + item.totalCost, 0)
}
